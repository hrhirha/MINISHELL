#include "libft.h"

size_t	ft_strlen(const char *s)
{
	size_t	num;
	size_t	i;

	if (s == NULL)
		return (0);
	num = 0;
	i = 0;
	while (s[i] != '\0')
	{
		num++;
		i++;
	}
	return (num);
}
