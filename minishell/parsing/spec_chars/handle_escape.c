#include "../../minishell.h"

char	*handle_escape(char *s, int *i)
{
	char *sub;

	if (s[*i] == '$' || s[*i] == '"' || s[*i] == '\\')
		*i++;
	sub = ft_substr(s, *i, 1);
	return (sub);
}
